    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String grupoSanguineo = (String) grupoSanguejComboBox1.getSelectedItem();
        String quantidadeText = quantidadejTextField1.getText();
        int quantidade = Integer.parseInt(quantidadeText);
        
        try{
            Connection con = ConexaoMysql.getConexaoMySQL();
            PreparedStatement ps = con.prepareStatement("update estoquedesangue set QUANTIDADE = (QUANTIDADE + ?) where GRUPOSANGUINEO = '" + grupoSanguineo + "'");
            ps.setInt(1, quantidade);
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Atualizado com Sucesso");
            setVisible(false);
            new AdicionarEstoque().setVisible(true);
            
           
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

