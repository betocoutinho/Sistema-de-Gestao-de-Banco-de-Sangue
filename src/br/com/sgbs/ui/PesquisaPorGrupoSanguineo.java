    private void grupoSanguejTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_grupoSanguejTextField1KeyReleased
        // TODO add your handling code here:
        String grupoSanguineo = grupoSanguejTextField1.getText();

        try{
            Connection con = ConexaoMysql.getConexaoMySQL();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from doador where GRUPOSANGUINEO like '%" + grupoSanguineo + "%'");
            jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_OFF);
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_grupoSanguejTextField1KeyReleased

